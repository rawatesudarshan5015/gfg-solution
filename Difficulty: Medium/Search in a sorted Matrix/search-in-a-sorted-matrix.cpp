class Solution {
  public:
    // Function to search a given number in row-column sorted matrix.
    bool searchMatrix(vector<vector<int>> &mat, int x) {
        // code here
        int i=0;
        int j=(mat[0].size()*mat.size())-1;
        
        while(i<=j){
            int mid = i+(j-i)/2;
            int row = mid/mat[0].size();
            int col = mid%mat[0].size();
            
            if(mat[row][col]==x){
                return true;
            }else if(mat[row][col]<x){
                i=mid+1;
            }else{
                j = mid-1;
            }
        }
        return false;
    }
};

